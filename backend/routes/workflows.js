const express = require('express');
const pool = require('../db');
const auth = require('../middleware/auth');
const router = express.Router();

router.get('/', auth, async (req, res) => {
  try {
    const result = await pool.query('SELECT * FROM workflows ORDER BY created_at DESC');
    res.json(result.rows);
  } catch (err) { res.status(500).json({ error: err.message }); }
});

router.get('/:id', auth, async (req, res) => {
  try {
    const result = await pool.query('SELECT * FROM workflows WHERE id = $1', [req.params.id]);
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json(result.rows[0]);
  } catch (err) { res.status(500).json({ error: err.message }); }
});

router.post('/', auth, async (req, res) => {
  try {
    const { name, description, steps, trigger_type, status, schedule } = req.body;
    const result = await pool.query(
      'INSERT INTO workflows (name, description, steps, trigger_type, status, schedule) VALUES ($1,$2,$3,$4,$5,$6) RETURNING *',
      [name, description, JSON.stringify(steps || []), trigger_type || 'manual', status || 'active', schedule]
    );
    res.status(201).json(result.rows[0]);
  } catch (err) { res.status(500).json({ error: err.message }); }
});

router.put('/:id', auth, async (req, res) => {
  try {
    const { name, description, steps, trigger_type, status, schedule } = req.body;
    const result = await pool.query(
      'UPDATE workflows SET name=$1, description=$2, steps=$3, trigger_type=$4, status=$5, schedule=$6, updated_at=NOW() WHERE id=$7 RETURNING *',
      [name, description, JSON.stringify(steps || []), trigger_type, status, schedule, req.params.id]
    );
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json(result.rows[0]);
  } catch (err) { res.status(500).json({ error: err.message }); }
});

router.delete('/:id', auth, async (req, res) => {
  try {
    const result = await pool.query('DELETE FROM workflows WHERE id = $1 RETURNING *', [req.params.id]);
    if (result.rows.length === 0) return res.status(404).json({ error: 'Not found' });
    res.json({ message: 'Deleted successfully' });
  } catch (err) { res.status(500).json({ error: err.message }); }
});

module.exports = router;
